#!/bin/bash
# ------------------------------------------------------------------------------
# copy whole project without .mat files 
# ------------------------------------------------------------------------------
printf "\n\n%-3s-----------------------------"
printf "\n%-4sclone a vault-[sy,fi] project."
printf "\n%-3s-------------------------------\n\n\n"
# ------------------------------------------------------------------------------
echo field data or synthetic data \(fi or sy\)?
read fi_sy
# ------------------------------------------------------------------------------
echo what name do you want?
read project_name_
# ------------------------------------------------------------------------------
project_name_=$project_name_-$fi_sy
# ------------------------------------------------------------------------------
vault_=vault-$fi_sy
# ------------------------------------------------------------------------------
mkdir $project_name_
# ------------------------------------------------------------------------------
cp -a $vault_/. $project_name_/
# ------------------------------------------------------------------------------