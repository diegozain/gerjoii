% ------------------------------------------------------------------------------
[epsi,epsi_] = w_colecole(eps_inf,eps_dc,omeg,tau,ap);
% ------------------------------------------------------------------------------
sige = sigdc + omeg.*epsi_*epso;
% ------------------------------------------------------------------------------
